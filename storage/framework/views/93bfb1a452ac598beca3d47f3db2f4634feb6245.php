<h4>1. A robo-adviser is JPMorgan Chase’s latest play for online investors
By Julia Horowitz, CNN Business, Updated 1728 GMT(0128 HKT) July 10,2019</h4>

<p>London (CNN Business) JPMorgan Chase is launching a new robo-adviser as it
makes another play for Millennial online investors. The company said Wednesday
that it will offer digital investment portfolios as part of its You Invest service,
which debuted last year. Robo-advisers, or digital platforms that provide
automated investing services, are becoming more popular, especially as passive
investing catches on.
Customers need at least $2,500 to open a portfolio. After entering their risk
profile, time horizon and investing goals, they'll be matched with a selection of
JPMorgan ETFs. The annual advisory fee is 0.35%.
You Invest, which offers free stock trading, has allowed JPMorgan Chase to go up
against discount brokerages such as Charles Schwab (SCHW), TD Ameritrade
(AMTD) and E*Trade (ETFC). Its new robo-adviser will compete with products
from companies like Morgan Stanley (MS) and Wealthfront.
JPMorgan (JPM), the largest US bank, provides services to about 61 million
American households. It's looking to strengthen its relationships with those
customers, 50 million of whom use mobile or online banking services.
It's also going after Millennials, a potentially lucrative demographic that's in the
process building wealth and could use more of the bank's services down the line.
Some of the bank's online efforts haven't panned out. Finn, its mobile-only
banking app for Millennials, lasted just a year after it was rolled out nationally.
The company is currently moving Finn customers over to its Chase app.</p>


<h4>2.Walmart wants robots in stores. Target doesn’t
By Nathaniel Meyersohn, CNN Business, Updated 2228 GMT(0628 HKT)
October24,2019</h4>

<p>New York (CNN Business) Walmart and Target are taking different approaches
to adding robots in stores, a split that will have an impact on the companies'
massive workforces and shape the future of automation in retail.
Both of these legacy brick-and-mortar companies are testing robots in their
warehouses. Walmart (WMT), the country's largest retailer and private employer,
expects to add self-driving robots that scrub floors to 1,860 of its more-than 4,700
US stores by February. It will also add robots that scan shelf inventory at 350
stores and bots at 1,700 stores that automatically scan boxes as they come off
delivery trucks and sort them by department onto conveyer belts.
Walmart's 178,000-square-foot supercenters are expensive to operate, especially
as more shoppers shift online. The company is adding robots to help it increase
worker productivity and control costs. Walmart has around one million hourly
workers in its stores.</p>

<p>The company says "smart assistants" will reduce the amount of time workers
spend on "repeatable, predictable and manual" tasks in stores, allowing workers to
switch to selling merchandise to shoppers and other customer service roles.
With the robots, Walmart will reduce the hours it assigns workers to unloading
boxes and mopping the floors in some stores. That will lead to some employee
attrition over time, the company says.</p>

<p>Grocery chains are also using robots in their stores. Stop & Shop and Giant Food
Stores, owned by Dutch retailer Ahold Delhaize, are placing "Marty," a tall, gray
robot with googly eyes, in close to 500 stores. The bot roams around stores
looking for spills. Albertsons is also testing a shelf-scanning robot in a few stores.
However, Target (TGT), which has around 1,850 US stores and 360,000 workers,
has eschewed a robot-worker strategy.</p>

<p>The retailer has added self-checkout and automatic cash-counting machines to
hundreds of stores in recent years. But Target CEO Brian Cornell said Wednesday
his chain will not bring on the type of robots Walmart has deployed in stores.
"You won't see robots in Target stores anytime soon. We really think, even in
today's environment, where people are talking about AI and robotics and different
elements of technology, the human touch still really matters," Cornell said. Target

announced it would spend an extra $50 million in payroll hours during the
holidays.</p>
<p>Carl Benedikt Frey, fellow at the Oxford Martin School at Oxford University and
author of The Technology Trap: Capital, Labor and Power in the Age of
Automation, said it makes less economic sense for Target to invest in robots than
Walmart because it has fewer workers and smaller stores. More than 80% of
Target's stores are under 170,000-square-feet.</p>
<p>"Tractors were first adopted on larger farms and only later trickled down to some
mid-sized ones as prices dropped," said Frey. "The same is true of robots in
warehouses and retail stores."</p>

<h4>Polarizing issue</h4>

<p> Walmart and Target's strategies are playing out as worker and public concern
about automation grows.</p>

<p>According to a 2017 Pew Research survey, 85% of Americans support measures
to restrict robots. Andrew Yang, Democratic candidate for president, has made
protecting workers against automation a pillar of his campaign.
There are more than 15 million retail workers in America today, and lower-skilled
jobs in the industry such as cashiers and clerks are among those vulnerable to
technology advances.</p>

<p>Between 6 million to 7.5 million existing retail jobs are at risk of being replaced
over the course of the next 10 years by some form of automation, estimated
financial services firm Cornerstone Capital Group in a 2017 study.</p>

<p>In the retail industry, "margin pressure has made automation a requirement, not a
choice," according to McKinsey analysts. "Automation will disproportionately
disrupt retail."</p>

<p>Marc Perrone, president of the United Food and Commercial Workers
International Union, which represents some workers at Stop & Shop and Giant,
said that companies that automate certain tasks are not "'freeing up' staff to work
with customers."</p>
<p>"The truth is, they are de-skilling jobs and cutting workers' hours to make a buck
providing less service to customers and chipping away at good jobs for
hardworking people," he said.</p>
<p>Automation is changing the nature of retail work, experts and Walmart leaders
say.</p>
<p>Martin Block, executive director of the Retail Analytics Council and professor at
Northwestern University, said robots in stores will increase demand for retail

workers, "transitioning them to doing different tasks, especially spending more
time with customers."
</p>
<p>"As we evolve, there are certain activities, certain jobs that'll go away, but new
jobs will be created," Walmart US CFO Michael Dastugue said at an analyst
conference in March.</p>
<p>Walmart expects to use some of the hours it saves because of the robots to assign
workers to newly-created roles, such as selecting customers' grocery pickup and
delivery orders. Walmart has created 50,000 of these new types of jobs over the
past three years.</p>
<p>"We're looking at our jobs through the lens of technology and asking ourselves
which human tasks add value — and which should be automated," CEO Doug
McMillon said last year. "The workplace is going to look different in a few years
— not just in retail, but across the economy."</p>
        

