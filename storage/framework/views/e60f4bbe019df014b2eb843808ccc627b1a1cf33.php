成,對於新創來說也是一大負擔,加上台灣 AI 人才缺乏,找到適合的人才難度
很高。</p>
<p>「我們非常歡迎對機器學習與電腦視覺技術感興趣的人才加入。」張友辰說。
Furbo 抓住 AI 的趨勢,在廣大的寵物市場中另闢新的戰場。</p>

<a href="https://technews.tw/2019/11/30/government-agencies-ai-big-data-technological-innovation/"><h3>司法院》判決書變白話</h3></a>

<p>酒駕輕判?系統教你看
有變革發動者當觸媒,轉動了年近半百的老國營公司。同樣的情景,發生在向
來給人嚴肅、不易親近的司法院中。</p>

<p>司法值得信賴嗎?相較美國民眾對司法權的信賴度高達 7 成,台灣司法院今年
7 月公布的最新民調顯示,國內僅 4 成民眾相信法官會公平公正審理。其實近
10 年間,台灣民眾對司法信賴度始終維持在這樣的「低檔」。</p>

<p>因為,司法與人民的距離太遠。你可能也有類似經驗:判決書上寫的明明都是
中文,但讀來卻如「有字天書」。</p>

<p>「有時候可能(長達)3、5 頁,才只是第一段......用字又艱澀。我們的判決
書,都是寫給上級審(法官)看的,不是給民眾看的,」不只一般人,台北地
方法院法官林孟皇也感嘆,司法高牆,有時是法律人自己「築」出來的,「有時
法官為展現專業而寫的裁判書字眼,根本不是現代人日常生活會用的」。</p>
<p>曾有 20 多年法官經歷的司法院資訊處長王金龍同樣有感,「人做一份工作久
了,就會從本位主義角度出發,但司法應該要親民,也該透明。」</p>        
<p>為了扭轉此況,王金龍與同事在今年 7 月,用科技打造了「裁判易讀小幫手」
系統,讓一般人閱讀判決書時,就像在看維基百科;只要將滑鼠游標移動到專
業名詞上,就會自動跳出白話文解釋。</p>
<p>甚至,他們訓練 AI「當法官」。例如發生酒駕案,可在司法院系統「一鍵按
下」後,系統會根據過去判決書中的各種酒駕量刑因子,跑出該案的可能刑
期,準確度高達 85%。這讓不懂法律的一般民眾,也能懂得判刑「行情」,同
時,讓判案法官不至於做出離譜的判決。</p>
<p>2 套系統,讓司法院在今年總統盃黑客松競賽中,從 132 個團隊中脫穎而出,
成為最優秀前 5 強。</p>

