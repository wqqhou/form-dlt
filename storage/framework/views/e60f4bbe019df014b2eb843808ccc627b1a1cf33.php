<a href="https://www.bnext.com.tw/article/47513/furbo-prime-day-ai"><h3>寵物攝影機 Furbo-智慧狗保姆</h3></a>

<p>廣大的市場吸引創業家投入還沒有被滿足的項目,在眾多項目中,「狗保母」潛
力無窮。如美國當紅寵物照顧平台 Rover.com 就幫忙媒合平台上的「狗保母」,
幫出遠門的飼主照顧寵物,平台已經吸引超過 10 萬名「狗保母」加入。</p>

<p>而張友辰也注意到了狗保母的需求,在 12 月 Furbo 最新功能上線:狗狗專屬通
知!Furbo 不靠人力,藉著 AI 科技中的電腦視覺(Computer Vision)技術達成
狗狗照護目標,讓飼主就算出門在外也能隨時掌控狗狗狀態。</p>

<p>舉例來說,當 Furbo 偵測到狗狗特定行為,例如奔跑,玩玩具或跳到家具上
時,就會立刻收到推播通知,狗狗面對攝影機時,Furbo 還會自動拍照,並傳
送狗狗的萌照到你的手機裡。</p>

<p>另外,還提供家中偵測功能,讓飼主知道是否有陌生人在家出沒。而且越頻繁
地使用 Furbo,Furbo 將能越精準地提供符合你與狗狗的專屬通知。</p>

<p>張友辰認為,AI 趨勢不可免,就像網際網路的興起,一定會發生,「你可以問自
己,現在的服務或產品不做網際網路服務會如何?」答案就呼之欲出了,另一件事就是考量競爭對手發展,「若他們做了我沒有做,那我會不會死得很慘?」</p>

<p>而且導入人工智慧並不需要花大錢,Google 與 Microsoft 等公司都提供大量的
Open API 與開源服務,大幅度降低工程師學習門檻。</p>

