<a href="https://buzzorange.com/techorange/2019/06/18/ai-robot-learn-english/"><h3>【不要再去補習班了】AI 機器人幫你練英文,課程量身打
造學費還更便宜!</h3></a>
<p>行動網路普及,現在只要一支手機就可以輕鬆接收資訊,透過各種資源學習英
語。然而,「互動」一直是英語學習的痛點,它是學英文的關鍵環節,但是實體
的補習班有時空限制;Tutor ABC、AmazingTalker 等平台雖然可以打破時空限制,媒合教師與學生,卻有收費高的問題。</p>
<p>近年 AI 蓬勃發展,並產生了各種應用。如果把 AI 導入英語學習,打造專屬
的教學機器人,依照學習者的程度,做出相對應的回饋,是否可以解決痛點,
讓學習者可以打破時空限制,不必付高昂的學費,並有效地提升英語能力?/p>
<p>以下,為你整理 AI 英語教學的兩大應用模式。</p>
<p>AI 對話機器人:因材施教,互動愈多教學品質愈好
目前市場上最常見 AI 學英語應用城程式就是聊天機器人了。對話過幾次之
後,機器人可以抓到學習者的英文程度,並調整對話內容,讓學習者可以更有
效率的學英文。此外,越常練習的話,機器人就可以得到更多的資訊,愈能抓
到學習者的優缺點,量身打造課程。也就是說,如果使用者練得愈勤,機器人
的效率就會更高,使用者就可以獲得更好的教育品質。</p>
<p>目前市場上有 Andy English Bot、SpeakBuddy、EF Hello、Mondly、Andy 等聊天
機器人,能夠與使用者互動,協助使用者學習英語。以日本的 SpeakBuddy 為
例,它會根據使用者的多益成績分級,並提供超過 30 種的對話情境,例如醫
院、職場等等;而 Andy English Bot 會教使用者講英文笑話。</p>
<p>除了對話,聊天機器人也會出單字填空或文法題目,幫使用者打英語基礎。</p>
<h4>透過類神經網路規劃課程,蒐集文章優化教學內容</h4>
<p>除了聊天機器人,市場上還有主打類神經網路的 AI 英語學習 APP,例如
Lingvist。該系統導入 AI 演算法,搭配大數據資料庫,提供使用者適合的英語
內容;並根據使用者的狀況,調整教學的節奏。</p>
<p>Lingvist 不只能與使用者對話,還能夠蒐集影集、新聞、書籍、文章等內容,用
大數據分析常見的單字與文法,提供使用者更貼近母語人士的內容。</p>
<h4>低成本、全年無休等優勢,解決透過實體教師學習的痛點</h4>
<p>不同於補習班或線上家教,這種 AI 英語教學機器人的最大特色就是「低價」
了。不同於人數、時間與體力都有限的實體老師,AI 英語機器人只要成功開
發,邊際成本幾乎是零,還能夠隨著使用者的增加而提升效能,因此可以提供
使用者費用極低的服務。</p>
<p>這些 AI 英語教學機器人大多有限制時數的「免費」服務,但只要採用付費方
案,就可以無限制的使用,而且費用不高。像 Lingvist,年付費方案只要 2699
元,每天花不到 8 元就可以練英語,遠低於補習班與家教的費用。/p>
<p>除了用低價享受個人化教學,這種聊天機器人還有個優點就是「全年無休」,使
用者想在半夜練英文都不是問題,不必受限於補習班時間;最後,它還可以避
免尷尬,因為機器人不會對你有看法,旁邊也沒有同學,可以讓使用者自在講
英文。</p>

<a href="https://technews.tw/2019/10/16/i-jy-sheng-wz-viscovery-1st-bread-identification/"><h3>當烘焙遇見人工智慧,一之軒推出全台第一 AI 麵包辨識結
帳</h3></a>
<p>一之軒總經理廖明堅表示,隨著人工智慧發展,越來越多行業開始擁抱 AI 技
術,甚至悄悄走入人們生活。他不斷思考,如何將 AI 應用在烘焙業。由於烘
焙店多數販售的是新鮮現做的麵包與西點,不適合在產品上貼標籤,於是他從
AI 臉部辨識技術得到靈感,想像若是能把這樣的人工智慧技術轉換辨識上百款
麵包,或許能提升門市結帳效率。</p>
<p>為了這個想法,一之軒花了兩年搜尋美國、日本、中國相關技術,並遍尋國內
外可合作的廠商,卻無一能滿足需求。直到遇見 Viscovery 公司,他們提出 AI
辨識麵包的解決方案,正符合廖明堅的想法,於是雙方迅速展開合作。</p>
<p>經過密集討論與規劃,9 月率先於一之軒台北市南京東路旗艦店上線。顧客結
帳時,先將麵包放至平台,門市人員僅需按下確認鈕,將肉眼辨識麵包與確認
品項的任務交給電腦,讓人力接續快速處理收銀與裝袋麵包。這套 AI 麵包辨
識結帳系統是電腦透過攝影機,依據麵包外型特徵辨識,且一次可辨識多款麵
包。上線之初,辨識準確度超過 95%,上線後,電腦透過資料回流與辨識模型
自我訓練,目前辨識準確度超過 99%。</p>  
<p>這套 AI 麵包辨識結帳系統在旗艦店預先實行的結果非常成功,目前已緊鑼密
鼓規劃導入全省 20 多家門市,且希望未來可延伸辨識其他非麵包類的商品如
禮盒。</p>
<p>深耕「Vision AI」技術多年的 Viscovery 公司營運長劉志錕表示,他對廖明堅的
先進想法印象深刻,並願意率先嘗試運用到烘焙業。這套 AI 麵包辨識結帳系
統以電腦視覺、人工智慧、深度學習的技術為基礎,讓電腦具備人的思維,學習辨識麵包,不受麵包外觀自然差異性的影響,如糖粉或配料多寡,都能精準
辨識麵包種類與數量,進而協助麵包業者如一之軒,加速麵包結帳、減少顧客
排隊等待時間、提升消費者的購物體驗。</p>
<p>一之軒門市同仁對於新科技的導入感到十分興奮,表示 AI 麵包辨識的使用在
尖峰時刻特別有感,可幫助店員快速消化大排長龍的結帳人潮,有效縮短客人
排隊等待時間。不同於以往的結帳方式,門市同仁無需一邊目視麵包類別,一
邊操作 POS 結帳系統,取而代之的是由 AI 麵包辨識系統自動判讀麵包,並一
次帶出所有品項與結帳金額,比以往節省約 1 倍時間,甚至等客人掏錢包同
時,先為下一位客人辨識麵包,大幅提升結帳效率。推出一個月來,不少客人
好奇詢問是怎麼做到自動辨識,也增添了購物的科技體驗。</p>
<p>廖明堅強調,從 20 年前個人電腦普及,到 10 年前智慧手機崛起,他看到這
一波的人工智慧趨勢不知不覺滲透到生活中。科技發展本就應該融入生活,幫
助人們解決問題、提升效率,烘焙業可因導入新科技,服務更上層樓,而省下
的時間,讓門市同仁做更有溫度的服務。因為「用心」的服務,是人工智慧絕
對無法取代的。</p>

