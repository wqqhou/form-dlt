<a href="https://scitechvista.nat.gov.tw/c/sTkT.htm"><h3>智慧照護與照護機器人</h3></a>
<p>2018 年,內政部指出台灣社會人口結構已正式進入高齡社會,人口老化的速度
快到難以想像,銀髮族的照護工作將何去何從?目前,台灣老人健康照護的需
求與照護人力快速失衡。老人照護除了亟需人力外,費用也大幅度提升,造成
許多家庭的龐大負荷,和人力資源供需不平衡的問題,現在大多仰賴國外看護
移工。但這並非長久之道,如何利用 AI 提升老人照護的效率與品質值得重視。</p>

<p>因應照護人力的短缺,可把智慧照護導入每個家庭。首先,希望能藉由遠端平
台利用網路讓銀髮族可以線上問診。其次,提供資訊互動平台,銀髮族每天可
在家自行測量生理指數,結合手機 APP 把資料上傳至雲端照護系統,使銀髮族
在家就能提供健康狀態,並與醫療人員聯繫。</p>

<p>近年來,國內外積極推出智慧健康照護機器人,銀髮族藉由聲控方式與機器人
對話,從對話與數據傳輸進行遠端諮詢與簡單健康檢測。機器人甚至還有陪伴
的功能,能緩解照護人力的不足。</p>

<p>對於銀髮長者而言,新的 AI 照護機器人將可提供日常運動教練、營養諮詢與輔
助醫療照護。在有醫療需求時,更可即時協助關注長者健康狀況,提供醫療與
照護,是子女有效關注年長父母的好幫手。另一方面,在一般民眾家中,居家
照護機器人藉由語音對話,依個人需求提供服務,使家中成員能享受簡單的醫
療服務與方便獲得生活資訊。孩童也能利用圖像表情和聲音互動學習,增添生
活樂趣。</p>

